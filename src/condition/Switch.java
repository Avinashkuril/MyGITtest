package condition;

public class Switch {

	public static void main(String[] args) {
		int year=5;
		switch (year) {
		case 1:System.out.println("You are in 1st year");
		break;
		case 2: System.out.println("You are in 2nd year");
		break;
		case 3:System.out.println("YOu are in 3rd year");
		break;
		case 4: System.out.println("You are in 4th year");
		break;
		default:System.out.println("Please enter valid input 1-4 year");
			break;
		}
				
	}

}
